DROP FUNCTION IF EXISTS ANO_BISSEXTO;

DELIMITER $$

CREATE FUNCTION ANO_BISSEXTO(ANO INT)
RETURNS VARCHAR(20)
NOT DETERMINISTIC
READS SQL DATA
	BEGIN
		IF (ANO % 4 = 0 AND ANO % 100 != 0) OR (ANO % 400 = 0) THEN
			RETURN 'É bissexto';
		ELSE
			RETURN 'Não é bissexto';
		END IF;
	END $$

DELIMITER ;

SELECT ANO_BISSEXTO(2024) AS "Resultado";

