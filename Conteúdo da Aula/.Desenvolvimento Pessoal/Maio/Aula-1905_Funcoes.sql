DROP FUNCTION IF EXISTS ALEATORIO;

DELIMITER $$

CREATE FUNCTION ALEATORIO()
RETURNS INT
NOT DETERMINISTIC
READS SQL DATA
	BEGIN
		DECLARE VALOR INT;
        SET VALOR = FLOOR((RAND()*1000)+1);
        RETURN VALOR;
    END $$
DELIMITER ;

SELECT ALEATORIO() AS "Número Aleatório";

