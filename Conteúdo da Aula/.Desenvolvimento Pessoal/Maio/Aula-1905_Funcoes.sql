DROP FUNCTION IF EXISTS ParImpar;

DELIMITER $$

CREATE FUNCTION ParImpar(NUMERO INT)
RETURNS VARCHAR(10)
DETERMINISTIC
	BEGIN
		IF NUMERO % 2 = 0 THEN
			RETURN 'PAR';
        ELSE
			RETURN 'IMPAR';
		END IF;
	END $$

DELIMITER ;

SELECT ParImpar(7);

